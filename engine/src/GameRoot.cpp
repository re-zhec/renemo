#include "GameRoot.hpp"

#include <boost/filesystem.hpp>

namespace nemo
{

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

namespace {
	namespace fs = boost::filesystem;
	const static fs::path root_dir_ = fs::current_path();
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

fs::path
GameRoot::getRootDir()
{
	return root_dir_;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

fs::path
GameRoot::getAssetDir()
{
	return getRootDir() / "asset";
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

fs::path
GameRoot::getSpriteDir()
{
	return getAssetDir() / "sprite";
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

} // namespace nemo