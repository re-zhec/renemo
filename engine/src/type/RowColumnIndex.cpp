#include "type/RowColumnIndex.hpp"

namespace nemo::type
{

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

constexpr
RowColumnIndex::RowColumnIndex()
noexcept
	: _r(0)
	, _c(0)
{
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

RowColumnIndex::RowColumnIndex(const row_t r, const column_t c)
noexcept
	: _r(static_cast< decltype(_r) >(r))
	, _c(static_cast< decltype(_c) >(c))
{
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

RowColumnIndex::RowColumnIndex(const row_column_pair_t rc)
noexcept
	: _r(static_cast< decltype(_r) >(rc.first))
	, _c(static_cast< decltype(_c) >(rc.second))
{
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

RowColumnIndex::RowColumnIndex(const std::array< unsigned, 2 > rc)
noexcept
	: _r(rc.front())
	, _c(rc.back())
{
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

bool 
RowColumnIndex::operator == (const RowColumnIndex rhs) 
const noexcept
{
	return _r == rhs._r && _c == rhs._c;
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

bool 
RowColumnIndex::operator != (const RowColumnIndex rhs) 
const noexcept
{
	return !(*this == rhs);
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

template< typename T >
sf::Vector2< T >
RowColumnIndex::sfVector2()
const noexcept
{
	// Columns are along x-axis, row along y-axis.
	return { static_cast< T >(_c), static_cast< T >(_r) };
}

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

template sf::Vector2< int >      RowColumnIndex::sfVector2() const noexcept;
template sf::Vector2< float >    RowColumnIndex::sfVector2() const noexcept;
template sf::Vector2< unsigned > RowColumnIndex::sfVector2() const noexcept;

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

} 